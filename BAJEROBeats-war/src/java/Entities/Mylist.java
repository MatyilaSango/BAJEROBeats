package Entities;

import java.io.Serializable;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author Sango
 */
@Entity
@Table(name = "mylist")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "Mylist.findAll", query = "SELECT m FROM Mylist m")
    , @NamedQuery(name = "Mylist.findById", query = "SELECT m FROM Mylist m WHERE m.id = :id")})
public class Mylist implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "ID")
    private Integer id;
    @JoinColumn(name = "ProductId", referencedColumnName = "ID")
    @ManyToOne(optional = false)
    private Products productId;
    @JoinColumn(name = "UserId", referencedColumnName = "ID")
    @ManyToOne(optional = false)
    private User userId;

    public Mylist() {
    }

    public Mylist(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Products getProductId() {
        return productId;
    }

    public void setProductId(Products productId) {
        this.productId = productId;
    }

    public User getUserId() {
        return userId;
    }

    public void setUserId(User userId) {
        this.userId = userId;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Mylist)) {
            return false;
        }
        Mylist other = (Mylist) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "Entities.Mylist[ id=" + id + " ]";
    }
    
}
